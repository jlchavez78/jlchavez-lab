    private Object getContact(String email) {
        Parameters p = new Parameters();
        p.setHapikey(key);
        SearchPayload pl = new SearchPayload();
        pl.setAfter(0);
        pl.setLimit(1);
        pl.setProperties(Arrays.asList("email", "firstname"));
        pl.setSorts(Arrays.asList("email"));
        FilterGroups fg = new FilterGroups();
        Filter f = new Filter();
        f.setOperator("EQ");
        f.setValue(email);
        f.setPropertyName("email");
        fg.setFilters(Arrays.asList(f));
        pl.setFilterGroups(Arrays.asList(fg));
        Object o = client.searchContact(pl, p);
        Object c = null;
        if (o != null) {
            LinkedHashMap<String, Object> li = (LinkedHashMap<String, Object>) o;
            if (!li.isEmpty()) {
                ArrayList<Contact> contacts = (ArrayList<Contact>) li.get("results");
                if (!contacts.isEmpty()) {
                    c = contacts.get(0);
                }
            }
        }
        return c;
    }

